package com.tinhvan.hd.dto;

public class ConfigCheckRecordsMobile {

    private String key;

    private String name;

    private String valueOle;

    private String valueChange;


    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValueOle() {
        return valueOle;
    }

    public void setValueOle(String valueOle) {
        this.valueOle = valueOle;
    }

    public String getValueChange() {
        return valueChange;
    }

    public void setValueChange(String valueChange) {
        this.valueChange = valueChange;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
